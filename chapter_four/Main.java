public class Main {
    public static void main(String[] args) {
        int number = -99;

        if (number > 0) {
            System.out.println("Number is greater than zero!");
        } else if (number < 0) {
            System.out.println("Number is less than zero!");
        } else {
            System.out.println("Number is equal to zero!");
        }
    }
}